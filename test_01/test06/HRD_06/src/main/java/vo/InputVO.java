package vo;

import java.sql.Date;

public class InputVO {
	private int r_no;
	private String j_jumin;
	private String h_code;
	private Date r_date;
	private int r_time;
	private String r_vcode;
	
	public int getR_no() {
		return r_no;
	}
	public void setR_no(int r_no) {
		this.r_no = r_no;
	}
	public String getJ_jumin() {
		return j_jumin;
	}
	public void setJ_jumin(String j_jumin) {
		this.j_jumin = j_jumin;
	}
	public String getH_code() {
		return h_code;
	}
	public void setH_code(String h_code) {
		this.h_code = h_code;
	}
	public Date getR_date() {
		return r_date;
	}
	public void setR_date(Date r_date) {
		this.r_date = r_date;
	}
	public int getR_time() {
		return r_time;
	}
	public void setR_time(int r_time) {
		this.r_time = r_time;
	}
	public String getR_vcode() {
		return r_vcode;
	}
	public void setR_vcode(String r_vcode) {
		this.r_vcode = r_vcode;
	}
	
	
}
