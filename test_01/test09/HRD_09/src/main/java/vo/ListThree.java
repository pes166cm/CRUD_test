package vo;

public class ListThree {
	private String artist_name;
	private String sum_point;
	private String avg_point;
	private String rank;
	
	public String getArtist_name() {
		return artist_name;
	}
	public void setArtist_name(String artist_name) {
		this.artist_name = artist_name;
	}
	public String getSum_point() {
		return sum_point;
	}
	public void setSum_point(String sum_point) {
		this.sum_point = sum_point;
	}
	public String getAvg_point() {
		return avg_point;
	}
	public void setAvg_point(String avg_point) {
		this.avg_point = avg_point;
	}
	public String getRank() {
		return rank;
	}
	public void setRank(String rank) {
		this.rank = rank;
	}
	
}
