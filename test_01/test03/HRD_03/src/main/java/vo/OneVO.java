package vo;

public class OneVO {
	private String c_no;
	private String p_name;
	private String p_code;
	private String c_school;
	private String c_jumin;
	private String c_city;
	private String p_tel;
	
	public String getC_no() {
		return c_no;
	}
	public void setC_no(String c_no) {
		this.c_no = c_no;
	}
	public String getP_name() {
		return p_name;
	}
	public void setP_name(String p_name) {
		this.p_name = p_name;
	}
	public String getP_code() {
		return p_code;
	}
	public void setP_code(String p_code) {
		this.p_code = p_code;
	}
	public String getC_school() {
		return c_school;
	}
	public void setC_school(String c_school) {
		this.c_school = c_school;
	}
	public String getC_jumin() {
		return c_jumin;
	}
	public void setC_jumin(String c_jumin) {
		this.c_jumin = c_jumin;
	}
	public String getC_city() {
		return c_city;
	}
	public void setC_city(String c_city) {
		this.c_city = c_city;
	}
	public String getP_tel() {
		return p_tel;
	}
	public void setP_tel(String p_tel) {
		this.p_tel = p_tel;
	}
	
}
